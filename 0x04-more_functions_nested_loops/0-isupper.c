#include <stdio.h>

/**
 * _isupper - print uppercase
 * @c: parameter char
 *
 * Return: 1 if upper, otherwise 0.
 */
int _isupper(int c)
	/** _isupper - print uppercase */
	/* @c: parameter char*/
	/* Return: 1 if upper, otherwise 0.*/
{
	if (c >= 'A' && c <= 'Z')
	{
		return (1);
		printf("n");
	}
	else
	{
		return (0);
		printf("n");
	}
}
